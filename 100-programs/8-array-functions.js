let names =["a", "d", "b", "c"];

console.log(names);

//add new element
names.push("e");
console.log(names);

//find count of an array
console.log(names.length);

//convert to string
console.log(names.toString());

//to string with custom character
console.log(names.join("-"));

//sort in order
console.log(names.sort());

//reverse order
console.log(names.reverse());
